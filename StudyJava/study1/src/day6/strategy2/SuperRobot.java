package day6.strategy2;

public class SuperRobot extends RobotFrame {

	public SuperRobot() {
		System.out.println("SuperRobot 입니다.");
	}
	
	@Override
	public void actionFly() {
		System.out.println("이 로봇은 날 수 있습니다.");
		
	}

	@Override
	public void actionMisail() {
		System.out.println("이 로봇은 미사일이 있습니다.");
		
	}

	@Override
	public void knife() {
		System.out.println("이 로봇은 레이저검이 있습니다.");
		
	}

}
